							<div class="text-descripcion">
								Por no contar con licencia de funcionamiento y persistir en la venta de bebidas alcohólicas, tres locales que operaban como bares clandestinos fueron clausurados definitivamente por la Municipalidad Distrital de Ciudad Nueva en un operativo realizado la noche del 06 de junio.<br> <br>
								Funcionarios del área de la Sub Gerencia de Administración Tributaria de la comuna Ciudad Nueva junto a personal de Seguridad Ciudadana, Policía Nacional y la Gobernación iniciaron el operativo con la intervención de la vivienda ubicada en el comité 02 Mz 06 Lt 17 donde aparentemente funcionaba una tienda de abarrotes, sin embargo, al interior de esta se expendían bebidas alcohólicas. El bar clandestino era conducido por Martha Maquera Bonifacio. <br> <br>
								Posteriormente, fue clausurado el bar clandestino de propiedad de Ana María Pérez Dueña, el cual está ubicado en el comité 03 Mz 21 Lt 20. Se expendían bebidas alcohólicas tal y como se pudo verificar al hallar reunido a un grupo de varones que se encontraban bebiendo cerveza.
									<br> <br>
								Finalmente, se procedió al cierre del bar ubicado en la Asociación de Vivienda 
								28 de Agosto comité 06 Mz 375 Lt 09 de propiedad de Flora Calisaya Huayhua. 
								La clausura definitiva de los tres bares clandestinos se efectuó en cumplimiento a la Ordenanza 
								Municipal N° 002-2009-MDCN-T, En dicha diligencia se les decomisaron las licencia que fueron otorgados
								para el expendio de abarrotes, también se impuso papeletas de multa por infringir con las normas de la ordenanza, por tal motivo los propietarios deberán pagar una multa de s/.385.00 nuevos soles.
