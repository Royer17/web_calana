								El Comité Distrital de Seguridad Ciudadana (CODISEC), presidida por el Alcalde de la
								 Municipalidad Distrital de Ciudad Nueva, Med. Edgar Concori Coaquira realizó la segunda
								  Consulta vecinal, la cual se llevó a cabo el viernes 26 de Junio en el ex comedor 
								  municipal, concerniente a la problemática de los bares y cantinas clandestinos que 
								  operan en el distrito. <br> <br>
