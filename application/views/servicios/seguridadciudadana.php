								   fecha, donde efectuó el cierre de 23 locales y/o establecimientos que se dedicaban a la venta
								    de bebidas alcohólicas en el interior y exterior de su local comercial. Esta medida fue efectuada 
								    debido a la proliferación de bares y cantinas en el Distrito, el cual es uno de los factores que incide
