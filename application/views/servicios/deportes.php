							<div class="text-descripcion">
								Niños, Adolecentes, Jóvenes y Adultos del distrito de Ciudad Nueva vibraron de alegría en la apertura del “Recreovia 2015”, la actividad se efectuó a las 8:00 horas y conto con la presencia de las autoridades de la jurisdicción, el escenario principal de los juegos recreativos fue la avenida internacional. <br> <br>
								El evento se inició con un trote motivacional encabezado por el teniente alcalde de la Municipalidad Distrital de Ciudad Nueva, Óscar Onque Calizaya y la presidenta de la Dirección Regional del Deporte, Ana María Stumber Gonzales, quienes acompañados de los estudiantes las instituciones educativas Mariscal Cáceres, Manuel A. Odria, Simón Bolívar y Cesar Cohaila Tamayo, trotaron por la avenida internacional incentivando el deporte saludable. <br> <br>
								Luego de la ceremonia de apertura, esta importante avenida fue utilizada exclusivamente por jóvenes estudiantes quienes se ejercitaron con las talleres de tae Bo, fulbito y vóley, asimismo compitieron en los concursos de la soga y ula ula. Mientras los niños se divirtieron con los juegos gigantes de ludo, damas y michí. Por otro lado, los adultos pudieron visitar los stands de módulo de promoción de la salud y módulo de alimentación saludable.
									<br> <br>
								El mencionado evento organizado por la Municipalidad de Ciudad Nueva, a través de la oficina de Cultura y Deporte tiene la finalidad de ofrecer a la población un espacio para la recreación gratuita y generar valores de convivencia ciudadana. Recreovia es una gran alternativa para pasar un día deportivo, y se realizará próximamente este 19 de julio a partir de las 9:00 a 12:00 horas.	

