<p>
	<strong>¿QUÉ ES UN PLAN DE CONTINGENCIA?</strong></p>
<p style="text-align: justify;">
	Es el establecimiento de estrategias, técnicas y procedimientos orientados a prevenir, controlar y evaluar los efectos producidos por cualquier tipo de siniestro.</p>
<p>
	<strong>¿QUÉ ES EL COMITÉ DE PERMANENTE DE DEFENSA CIVIL?</strong></p>
<p style="text-align: justify;">
	Es el equipo integrado por personal que labora en el Palacio Municipal tiene como función principal apoyar en la estructuración del Plan de Contingencia, estableciendo estrategias y medidas preventivas y/o correctivas para enfrentar exitosamente cualquier tipo de desastre, así como actuar inmediatamente durante el siniestro.</p>
<p style="text-align: justify;">
	<strong>¿QUÉ ES UN SINIESTRO?</strong></p>
<p style="text-align: justify;">
	Se considera siniestro a todo evento, ya sea de origen natural o Tecnológico, que ponga en riesgo la vida de los seres humanos, así como de sus espacios y pertenencias.</p>
<p style="text-align: justify;">
	<strong>¿QUÉ ES UN SINIESTRO TECNOLOGICO?</strong></p>
<p style="text-align: justify;">
	Son los siniestros provocados accidental o intencionalmente por el hombre que puede provocar una situación de emergencia y/o desastres.</p>
